    /**
     * 旋转图片
     * @param bitmap
     * @param filePath
     * @return
     */
    public static boolean rotateBitmap(Bitmap bitmap, String filePath) {
        boolean result = false;
        BufferedOutputStream bos = null;
        File file = null;
        try {
            Bitmap bMapRotate;
            Matrix matrix = new Matrix();
            matrix.reset();
            matrix.postRotate(-90);
            bMapRotate = Bitmap.createBitmap(bitmap, 0, 0, bitmap.getWidth(),
                    bitmap.getHeight(), matrix, true);
            bitmap = bMapRotate;
            //2.保存文件
            File dir = new File(filePath);
            if (!dir.exists() && dir.isDirectory()) {
                dir.mkdirs();
            }
            file = new File(filePath);
            bos = new BufferedOutputStream(new FileOutputStream(file));
            bitmap.compress(Bitmap.CompressFormat.JPEG, 100, bos);//将图片压缩到流中
            bos.flush();//输出
            bos.close();//关闭
            result = true;
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            if (bos != null) {
                try {
                    bos.close();
                } catch (IOException e) {
                    e.printStackTrace();
                }
            }
        }
        return result;
    }

    /**
     * asset资源保存到本地
     *
     * @param context        上下文
     * @param assetPath      asset下的路径
     * @param targetFilePath targetFilePath
     */
    public static void assetToSdcard(Context context, String assetPath, String targetFilePath) {
        AssetManager asset = context.getAssets();
        //循环的读取asset下的文件，并且写入到SD卡
        String[] filenames = null;
        FileOutputStream out = null;
        InputStream in = null;
        try {
            filenames = asset.list(assetPath);
            if (filenames.length > 0) {//说明是目录
//				列表文件
            } else {//说明是文件，直接复制到SD卡
                File SDFlie = new File(targetFilePath);
                if (!SDFlie.exists()) {
                    SDFlie.createNewFile();
                }
                //将内容写入到文件中
                in = asset.open(assetPath);
                out = new FileOutputStream(SDFlie);
                byte[] buffer = new byte[1024];
                int byteCount = 0;
                while ((byteCount = in.read(buffer)) != -1) {
                    out.write(buffer, 0, byteCount);
                }
                out.flush();
            }
        } catch (IOException e) {
            e.printStackTrace();
        } finally {
            try {
                out.close();
                in.close();
            } catch (IOException e) {
                e.printStackTrace();
            }
        }
    }

